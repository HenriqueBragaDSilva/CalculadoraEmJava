    private int calcular(int num1, char op, int num2){   
     int res = 0;  
 
     if(op == '*'){  
          res = num1 * num2;   
     }
     else if(op == '+'){ 
          res = num1 + num2;
     }
     else if(op == '-'){ 
          res = num1 - num2;
     }
     else if(op == '/'){ 
          res = num1 / num2;
     }
         return res;   
     }
    
